void main() {
  // Integer
  /**
   * Integer will store decimal number
   */
  int number1 = 12;
  print("integer number = $number1");

  /**
   * Double will store number that have a commas
   */
  // Double
  double number2 = 10.5;
  print("double number = $number2");

  // num
  /**
   * num is flexible, it can store integer or double
   */
  num numbernum = 12;
  print(numbernum);
  numbernum = 11.2;
  print(numbernum);
}
