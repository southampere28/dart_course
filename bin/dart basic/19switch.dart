void main(List<String> args) {
  String mark = 'E';

  switch (mark) {
    case 'A':
      print('you are passed with great mark!');
      break;
    case 'B':
      print('good grade');
      break;
    case 'C':
      print('you are passed');
      break;
    case 'D':
      print('you are fail');
      break;
    default:
      print('wrong input');
  }
}
